const fs = require('fs');
const path = require('path');

// 读取CSV文件
const csvPath = path.join(__dirname, '..', '..', 'plan', '黄大仙灵签100支完整版.csv');
const csvContent = fs.readFileSync(csvPath, 'utf-8');

// 解析CSV
const lines = csvContent.trim().split('\n');
const headers = lines[0].split(',');

const sticks = [];
for (let i = 1; i < lines.length; i++) {
  const line = lines[i].trim();
  if (!line) continue;

  // 简单解析CSV（假设没有逗号在引号内）
  const cols = line.split(',');

  const id = parseInt(cols[0], 10);
  if (isNaN(id)) continue;

  const level = cols[1] || '中平签';
  const title = cols[2] || '';
  const poem = cols[3] || '';
  const meaning = cols[4] || '';
  const advice = cols[5] || '';

  // 生成典故故事（基于标题）
  const story = generateStory(title);

  sticks.push({
    id,
    level,
    title,
    poem,
    meaning,
    advice,
    story
  });
}

